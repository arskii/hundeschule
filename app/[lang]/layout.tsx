import { Catamaran, IBM_Plex_Mono } from 'next/font/google'
import { Locale, i18n } from '../../i18n-config'

export async function generateStaticParams() {
	return i18n.locales.map(locale => ({ lang: locale }))
}

import Footer from '@component/components/Footer'
import Header from '@component/components/Header'
import { ScrollToTopButton } from '@component/components/ToTopButton'
import { getDictionary } from '@component/get-dictionary'
import './globals.css'

const ibm_plex_mono = IBM_Plex_Mono({
	display: 'block',
	variable: '--font-ibm_plex_mono',
	weight: ['500', '400'],
	subsets: ['latin'],
})

const catamaran = Catamaran({
	display: 'block',
	variable: '--font-catamaran',
	weight: ['500', '400'],
	subsets: ['latin'],
})

export const metadata = {
	title: 'Hundeschule',
	description: 'Generated by create next app',
}

export default async function RootLayout({
	children,
	params,
}: {
	children: React.ReactNode
	params: { lang: Locale }
}) {
	const dictionary = await getDictionary(params.lang)
	return (
		<html
			lang={params.lang}
			className={`${ibm_plex_mono.variable} ${catamaran.variable}`}
		>
			<body>
				<Header dictionary={dictionary} />
				{children}
				<ScrollToTopButton />
				<Footer />
			</body>
		</html>
	)
}
